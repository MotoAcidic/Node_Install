HEIGHT=15
WIDTH=40
CHOICE_HEIGHT=6
BACKTITLE="Node Install Setup Wizard"
TITLE="Node Install Setup"
MENU="Choose one of the following bases to compile from:"

OPTIONS=(1 "Bitcoin based source"
		 2 "Dash based source"
		 3 "Pivx based source"
		 4 "Mining Pool Setup"

		 0 "Exit Script"
)

CHOICE=$(whiptail --clear\
		--backtitle "$BACKTITLE" \
                --title "$TITLE" \
                --menu "$MENU" \
                $HEIGHT $WIDTH $CHOICE_HEIGHT \
                "${OPTIONS[@]}" \
                2>&1 >/dev/tty)

clear
case $CHOICE in
        0)  # Exit
		exit	
		;;

        1)	# BTC Source
		bash MasternodeInstall.sh
        ;; 

		2)	# Dash Source
		bash MasternodeUpdate.sh
        ;;
		
		3)	# Pivx Source
		bash CompileWindows.sh
        ;;

		5)	#Pool Install
		cd
		cd Node_Install/Pool/
		bash install.sh
        ;;

esac